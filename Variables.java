public class Variables {
    public static void main(String[] args) {
        int a = 10;
        char b = 'a';
        double c = 109.23d;
        float d = 10.3f;
        String s = "Bhavin";
        boolean bo = true;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(s);
        System.out.println(bo);
    }
}
